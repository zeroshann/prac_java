public class counting_people {
    
    public static void main(String[] args) {
        // Initialize the number of people
        int people = 0;

        people++;
        people++;
        people++;

        System.out.println("Number of people: " + people); // Outputs 3

        people--;
        System.out.println("Number of people after one leaves: " + people); // Outputs

    }
}
